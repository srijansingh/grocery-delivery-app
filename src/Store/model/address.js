class Address{
    constructor(id, userid, name, mobile, pincode, address, locality, district, state){
        this.id=id;
        this.userid = userid;
        this.name = name;
        this.mobile = mobile;
        this.pincode = pincode;
        this.locality = locality;
        this.address = address;
        this.district = district;
        this.state = state;
    }
}

export default Address